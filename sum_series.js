
     
function sumOfArithmeticSeries(n) {
    // Calculate the sum of the arithmetic series
    const sum = (n * (n + 1)) / 2;
  
    // Return the sum
    return sum;
  }
  
  // Example usage:
  
  const n = 10;
  const sum = sumOfArithmeticSeries(n);
  
  console.log(sum); // Output: 55